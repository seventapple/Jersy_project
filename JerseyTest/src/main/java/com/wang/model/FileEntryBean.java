package com.wang.model;

public class FileEntryBean {
private boolean fileFlg;
private String name;
	private int price;

	public boolean isFileFlg() {
		return fileFlg;
	}

	public void setFileFlg(boolean fileFlg) {
		this.fileFlg = fileFlg;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}
}
